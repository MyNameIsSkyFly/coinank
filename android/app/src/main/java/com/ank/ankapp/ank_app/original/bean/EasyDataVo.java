package com.ank.ankapp.ank_app.original.bean;

public class EasyDataVo {

    private String liquidation;
    private String ticker;
    private String openInterest;
    private String shortRatio;
    private String longRatio;

    public String getLiquidation() {
        return liquidation;
    }

    public void setLiquidation(String liquidation) {
        this.liquidation = liquidation;
    }

    public String getTicker() {
        return ticker;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }

    public String getOpenInterest() {
        return openInterest;
    }

    public void setOpenInterest(String openInterest) {
        this.openInterest = openInterest;
    }

    public String getShortRatio() {
        return shortRatio;
    }

    public void setShortRatio(String shortRatio) {
        this.shortRatio = shortRatio;
    }

    public String getLongRatio() {
        return longRatio;
    }

    public void setLongRatio(String longRatio) {
        this.longRatio = longRatio;
    }
}
