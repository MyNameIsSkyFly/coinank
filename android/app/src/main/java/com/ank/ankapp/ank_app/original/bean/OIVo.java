package com.ank.ankapp.ank_app.original.bean;

public class OIVo {

    private float coinCount;
    private float coinValue;
    private String baseCoin;
    private String exchangeName;
    private float rate;
    private float change1H;
    private float change4H;
    private float change24H;

    public float getCoinCount() {
        return coinCount;
    }

    public void setCoinCount(float coinCount) {
        this.coinCount = coinCount;
    }

    public float getCoinValue() {
        return coinValue;
    }

    public void setCoinValue(float coinValue) {
        this.coinValue = coinValue;
    }

    public String getBaseCoin() {
        return baseCoin;
    }

    public void setBaseCoin(String baseCoin) {
        this.baseCoin = baseCoin;
    }

    public String getExchangeName() {
        return exchangeName;
    }

    public void setExchangeName(String exchangeName) {
        this.exchangeName = exchangeName;
    }

    public float getRate() {
        return rate;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }

    public float getChange1H() {
        return change1H;
    }

    public void setChange1H(float change1H) {
        this.change1H = change1H;
    }

    public float getChange4H() {
        return change4H;
    }

    public void setChange4H(float change4H) {
        this.change4H = change4H;
    }

    public float getChange24H() {
        return change24H;
    }

    public void setChange24H(float change24H) {
        this.change24H = change24H;
    }
}
